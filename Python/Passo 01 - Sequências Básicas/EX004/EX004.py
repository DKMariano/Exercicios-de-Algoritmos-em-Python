# Descrição: Desenvolva um algoritmo que leia dois números inteiros e mostre o somatório entre eles.

# Solicita ao usuário que insira o primeiro e o segundo número inteiro
numero1 = int(input("\nDigite o primeiro número inteiro: "))
numero2 = int(input("Digite o segundo número inteiro: "))

# Calcula a soma dos dois números inteiros
soma = numero1 + numero2

# Exibe o resultado da soma
print(numero1, "+", numero2, "=", soma)
